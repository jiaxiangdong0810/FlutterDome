import 'package:intl/intl.dart' as intl;
import 'app_localizations.dart';

// ignore_for_file: type=lint

/// The translations for English (`en`).
class AppLocalizationsEn extends AppLocalizations {
  AppLocalizationsEn([String locale = 'en']) : super(locale);

  @override
  String get pageTitle => 'Localization Demo';

  @override
  String currentLocale(String locale) {
    return 'Current locale: $locale';
  }

  @override
  String greeting(String name) {
    return 'Hello, $name!';
  }

  @override
  String itemCount(int count) {
    return 'You have $count messages';
  }

  @override
  String get switchToEn => 'Switch to English';

  @override
  String get switchToZh => 'Switch to Chinese';

  @override
  String get demoDescription =>
      'This page demonstrates flutter gen-l10n. Translations are defined in .arb files, and Dart code is generated by running flutter gen-l10n.';
}
